    public void setData(){
        while (((DefaultTableModel)jTable1.getModel()).getRowCount()>0)
          {
             ((DefaultTableModel)jTable1.getModel()).removeRow(0);
          }
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object rowData[]  = new Object[5];
        for(int i = 0 ;i < roomService.getRooms().size();i++){
            
            String roomType= roomService.getRoomTypes().get(roomService.getRooms().get(i).getRoomType() - 1);
            String roomView= roomService.getRoomViews().get(roomService.getRooms().get(i).getRoomView() - 1);
            String roomStatus= roomService.getRoomStatus().get(roomService.getRooms().get(i).getRoomStatus() - 1);
            
            rowData[0] = roomService.getRooms().get(i).getId();
            rowData[1] = roomType;
            rowData[2] = roomView;
            rowData[3] = roomStatus;
            rowData[4] = roomService.getRooms().get(i).getRoomNumber();
            model.addRow(rowData);
        }        
    }

