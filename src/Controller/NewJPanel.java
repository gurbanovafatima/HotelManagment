    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if(jTable1.getSelectedRow() >= 0){
        int id = Integer.parseInt(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0).toString());
        roomService.DeleteById(id);
        ((DefaultTableModel)jTable1.getModel()).removeRow(jTable1.getSelectedRow());
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonAddRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddRoomActionPerformed

       Room room = new Room(jComboBoxRoomType.getSelectedIndex(), jComboBoxRoomPropert.getSelectedIndex(),
            jComboBoxRoomStatus.getSelectedIndex(),jTextFieldRoomNumber.getText());
        int id = Integer.parseInt(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0).toString());
        room.setId(id);
        roomService.Update(room);
        jPanelRoom.setVisible(false);
        setData();
       
    }//GEN-LAST:event_jButtonAddRoomActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
       if(jTable1.getSelectedRow() >= 0){
            int id = Integer.parseInt(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0).toString());
            Room room =  roomService.findById(id);
           jComboBoxRoomType.setSelectedIndex(room.getRoomType());
           jComboBoxRoomPropert.setSelectedIndex(room.getRoomView());
           jComboBoxRoomStatus.setSelectedIndex(room.getRoomStatus());
           jTextFieldRoomNumber.setText(room.getRoomNumber());
           jPanelRoom.setVisible(true);
       }
    }//GEN-LAST:event_jButtonUpdateActionPerformed


